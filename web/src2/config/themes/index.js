// 默认主题 ID
export const DEFAULT_THEME_ID = 'default-blue';

// 根据 ID 获取主题
export const getThemeById = (id) => {
  return presetThemes.find((theme) => theme.id === id) || presetThemes[0];
};

// 获取主题的颜色方案
export const getThemeColors = (themeId, mode) => {
  const theme = getThemeById(themeId);
  return theme.colors[mode] || theme.colors.light;
};
